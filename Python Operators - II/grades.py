grade= int(input("enter your grade (1 to 10) :"))
if grade >=1 and grade <=3 :
    print("basic level")
elif grade >=4 and grade <=5 :
    print("intermediate level")
elif grade >=6 and grade <=8 :
    print("advance level")
elif grade >=9 and grade <=10 :
    print("expertise level")
else :
    print ("invalid input")
    
    

    